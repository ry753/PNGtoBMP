#include<stdio.h>
#include"define_type.h"

/*================================================================================
エンディアン変換用関数
================================================================================*/
INTE endian_conv(INTE bit) {

	bit = ((bit >> 24) & 0x000000FF) |
		  ((bit >> 8 ) & 0x0000FF00) |
		  ((bit << 8 ) & 0x00FF0000) |
		  ((bit << 24) & 0xFF000000);

	return bit;
}